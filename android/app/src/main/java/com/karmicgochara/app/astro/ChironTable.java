    /**
     * Retourne la longitude sidérale de Chiron pour un JD donné.
     * Interpolation linéaire entre les deux entrées encadrantes.
     */
    public static double lonForJd(double jd) {
        double offset = (jd - JD_START) / STEP_DAYS;
        int    idx    = (int) offset;
        if (idx < 0)               return LON[0];
        if (idx >= LON.length - 1) return LON[LON.length - 1];
        double frac = offset - idx;
        double a = LON[idx], b = LON[idx + 1];
        // Gère le saut 360°→0° (Chiron franchit 0° vers ~2028)
        double diff = b - a;
        if (diff >  180) diff -= 360;
        if (diff < -180) diff += 360;
        return (a + frac * diff + 360) % 360;
    }
